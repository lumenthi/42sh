#include "../../includes/shell.h"

void	ft_tabdel(char ***tabs)
{
	size_t	i;

	i = 0;
	while (*(*tabs + i))
	{
		ft_strdel((*tabs + i));
		i++;
	}
	ft_strdel(*tabs);
}
